    private void jButtonWybierzActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonWybierzActionPerformed
        Pozycja poz = new Pozycja();
        Towar tow = new Towar();
        tow.znajdzTowar(idpress);
        
        poz.setNazwa(tow.getNazwa());
        poz.setPkwiu(tow.getPkwiu());
        poz.setIlosc(Double.parseDouble(this.jTextFieldIlosc.getText()));

        NumberFormat nf = NumberFormat.getInstance();
        nf.setMaximumFractionDigits(2);
        nf.setMinimumFractionDigits(2);
        nf.setGroupingUsed(false);

        poz.setCena_netto(tow.getCena_netto());
        poz.setCena_brutto(tow.getCena_brutto());
        poz.setVat(tow.getVat());
        poz.setWartosc_netto(Double.parseDouble((nf.format(tow.getCena_netto()*poz.getIlosc())).replaceAll(",", ".")));
        poz.setWartosc_brutto(Double.parseDouble((nf.format(tow.getCena_netto()*poz.getIlosc()*(100 + tow.getVat())/100)).replaceAll(",", ".")));

        this.fak.dodajPozycje(poz);
        this.fakgui.ListaPozycji();
        
        this.dispose();
        //this.fak.setId_kontrahent(idpress);
        //this.fak.setNr("z okna wyzej");
    }//GEN-LAST:event_jButtonWybierzActionPerformed


     public void ListaTowarow() {
        DefaultTableModel table = new DefaultTableModel();
        Towar objTowar = new Towar();
        ArrayList<Towar> listaTowarow = new ArrayList();
        listaTowarow = objTowar.ListaTowarow();

        table.addColumn("id");
        table.addColumn("Skrót");
        table.addColumn("Nazwa");
        table.addColumn("Cena Netto");
        table.addColumn("VAT");
        table.addColumn("Cena Brutto");
        table.setRowCount(listaTowarow.size());
        int i = 0;

        for (Towar x : listaTowarow) {
            table.setValueAt(x.getId(), i, 0);
            table.setValueAt(x.getSkrot(), i, 1);
            table.setValueAt(x.getNazwa(), i, 2);
            table.setValueAt(x.getCena_netto(), i, 3);
            table.setValueAt(x.getVat(), i, 4);
            table.setValueAt(x.getCena_brutto(), i, 5);
            i++;
        }

        this.jTable1.setModel(table);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(0);


    }


